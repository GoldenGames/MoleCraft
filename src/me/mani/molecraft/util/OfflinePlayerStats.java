package me.mani.molecraft.util;

public class OfflinePlayerStats {
	
	private int points;
	private int kills;
	private int deaths;
	private int wins;
	private int games;
	private int chests;

	public int getPoints() {
		return points;
	}
	
	public void setPoints(int points) {
		this.points = points;
	}
	
	public int getKills() {
		return kills;
	}
	
	public void setKills(int kills) {
		this.kills = kills;
	}
	
	public int getDeaths() {
		return deaths;
	}
	
	public void setDeaths(int deaths) {
		this.deaths = deaths;
	}
	
	public int getWins() {
		return wins;
	}
	
	public void setWins(int wins) {
		this.wins = wins;
	}
	
	public int getGames() {
		return games;
	}
	
	public void setGames(int games) {
		this.games = games;
	}
	
	public int getChests() {
		return chests;
	}
	
	public void setChests(int chests) {
		this.chests = chests;
	}
	
}
